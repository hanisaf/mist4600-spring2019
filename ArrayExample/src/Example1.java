
public class Example1 {

	public static void main(String[] args) {
	     int[] itemCounts; //declaration

	     itemCounts = new int[3]; //initialization
	     
	     itemCounts[0] = 122; //accessing, "write"
	     itemCounts[1] = 119;
	     itemCounts[2] = 117;

	     System.out.println(itemCounts[1]); //accessing, "read"
	     
	     String[] words = new String[3]; //declaration and initialization
	     words[0] = "Java";
	     words[1] = "Coffee";
	     words[2] = "Bean";
	     
	     System.out.println(words);
	     
 
	}

}
